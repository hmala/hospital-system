        RadiologyType::truncate();
        
        // سأضيف البيانات على دفعات عبر دالة مساعدة
        $this->insertRadiologyData();
    }
    
    private function insertRadiologyData()
    {
        $data = $this->getRadiologyTypesData();
        $usedCodes = [];
        
        foreach ($data as $item) {
            $baseCode = $this->generateCode($item[3]);
            $code = $baseCode;
            $counter = 1;
            
            // تجنب تكرار الأكواد
            while (in_array($code, $usedCodes)) {
                $code = $baseCode . '_' . $counter;
                $counter++;
            }
            
            $usedCodes[] = $code;
            
            RadiologyType::create([
                'main_category' => $item[0],
                'subcategory' => $item[1],
                'name' => $item[2],
                'code' => $code,
                'description' => $item[3],
                'base_price' => $item[4] ?: 0,
                'estimated_duration' => 20,
                'requires_contrast' => $this->requiresContrast($item[2]),
                'requires_preparation' => $this->requiresContrast($item[2]),
                'preparation_instructions' => $this->requiresContrast($item[2]) ? 'حسب التعليمات' : null,
                'is_active' => true,
                'notes' => $item[5] ?: null,
            ]);
        }
    }
    
    private function generateCode($englishName)
    {
        $code = strtoupper(preg_replace('/[^A-Z0-9]+/i', '_', $englishName));
        $code = trim($code, '_');
        return $code ?: ('CODE_' . substr(md5($englishName), 0, 8));
    }
    
    private function requiresContrast($arabicName)
    {
        return (stripos($arabicName, 'ملونة') !== false || 
                stripos($arabicName, 'صبغة') !== false || 
                stripos($arabicName, 'باريوم') !== false ||
                stripos($arabicName, 'تباين') !== false ||
                stripos($arabicName, 'contrast') !== false);
    }
    
